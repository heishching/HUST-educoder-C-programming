/*编写一个程序，从终端输入一个字符，判断该字符是否是十六进制数字字符，如果是，则输出该数字字符对应的整数，否则以十进制形式输出该字符对应的ASCII码。
例：
输入字符：‘a’    //a是十六进制字符；
输出结果：   10       //打印输出对应的十六进制整数10。

输入字符：‘g’     //g不是进制字符；
输出结果：  103      //g对应的ASCII值为103。
根据提示，在右侧Begin-End区域补充代码，编写一个完整的程序。（要求此题只能使用第二、三两章的知识完成，不建议使用条件语句与循环语句等）
*/
#include <stdio.h>

int main() {
/**********Begin**********/
    char ch;
    scanf("%c", &ch);

    // 手动判断是否是十六进制数字字符
    if ((ch >= '0' && ch <= '9') || (ch >= 'a' && ch <= 'f') || (ch >= 'A' && ch <= 'F')) {
        if ((ch >= '0' && ch <= '9')) {
            printf("%d\n", ch - '0');  // 数字字符直接转换为整数
        } else if ((ch >= 'a' && ch <= 'f')) {
            printf("%d\n", ch - 'a' + 10);  // 小写字母转换为10-15
        } else {
            printf("%d\n", ch - 'A' + 10);  // 大写字母转换为10-15
        }
    } else {
        // 如果不是十六进制数字字符，输出ASCII码
        printf("%d\n", (int)ch);}
/**********End**********/
	return 0;
}